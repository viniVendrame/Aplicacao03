package Entidades;

/**
 *
 * @author pedro
 */
public class Produtos {
    private String nome;
    private Double price;
    
    public Produtos(){}

    public Produtos(String nome, double price) {
        this.nome = nome;
        this.price = price;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    
}
